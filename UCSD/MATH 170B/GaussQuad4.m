function I = GaussQuad4(Fun)
    c0 = 1/2 - 30^(1/2)/36;
    c1 = 30^(1/2)/36 + 1/2;
    c2 = 30^(1/2)/36 + 1/2;
    c3 = 1/2 - 30^(1/2)/36;
    x0 = -((2*30^(1/2))/35 + 3/7)^(1/2);
    x1 = -(7^(1/2)*(3 - (2*30^(1/2))/5)^(1/2))/7;
    x2 = (7^(1/2)*(3 - (2*30^(1/2))/5)^(1/2))/7;
    x3 = ((2*30^(1/2))/35 + 3/7)^(1/2);

    I = c0*Fun(x0)+c1*Fun(x1)+c2*Fun(x2)+c3*Fun(x3);
end